const std = @import("std");
const fuzzig = @import("fuzzig");
const bmark = @import("benchmarks.zig");

pub fn main() !void {
    var gpa = std.heap.GeneralPurposeAllocator(.{}){};
    defer _ = gpa.deinit();

    const alloc = gpa.allocator();

    var finder = try fuzzig.Ascii.init(alloc, 6000, 500, .{});
    defer finder.deinit();

    const scores = try alloc.alloc(i32, LINES.len);
    defer alloc.free(scores);

    var time = try std.time.Timer.start();
    for (LINES, scores) |line, *s| {
        s.* = finder.score(line, "a") orelse 0;
    }
    const end = time.lap();
    std.debug.print("Elapsed: {s}\n", .{std.fmt.fmtDuration(end)});

    {
        const score = finder.score(
            "hello world this is a short message about things",
            "short about",
        ).?;
        std.debug.print("Passed 1: {any} \n", .{score == 217});
    }
    {
        const score = finder.score(
            "hello world this is a short message about things",
            "short abut",
        ).?;
        std.debug.print("Passed 2: {any} \n", .{score == 189});
    }

    var result = try bmark.benchmark(
        alloc,
        runBmark,
        .{&finder},
        .{ .warmup = 10, .trials = 100 },
    );
    defer result.deinit();

    result.printSummary();
}

pub fn runBmark(finder: *fuzzig.Ascii) void {
    // const score = finder.score(
    //     "hello world this is a short message about things" ** 100,
    //     "short abut thns",
    // ).?;
    const haystacks = .{
        "print", "println", "prelude", "println!", "prefetch", "prefix", "prefix!", "print!",
    };
    var score: ?i32 = null;
    inline for (haystacks) |hs| {
        score = finder.score(hs, "pre");
    }
    std.mem.doNotOptimizeAway(score);
}

